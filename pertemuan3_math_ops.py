# File ini akan berisi beberapa fungsi matematika sederhana

# Fungsi untuk penjumlahan
def tambah(a, b):
    return a + b

# Fungsi untuk pengurangan
def kurang(a, b):
    return a - b

# Fungsi untuk perkalian
def kali(a, b):
    return a * b

# Fungsi untuk pembagian
def bagi(a, b):
    if b != 0:
        return a / b
    else:
        return "Pembagian dengan nol tidak diperbolehkan."